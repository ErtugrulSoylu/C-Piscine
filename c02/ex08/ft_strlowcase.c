char	*ft_strlowcase(char *str)
{
	char	*ret;

	ret = str;
	while (*str != '\0')
	{
		if (*str <= 'Z' && *str >= 'A')
			*str += 'a' - 'A';
		str++;
	}
	return (ret);
}
