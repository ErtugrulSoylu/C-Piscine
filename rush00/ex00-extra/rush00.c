void	ft_putchar(char c);

void	rush04(int x, int y)
{
	int	i;
	int	j;

	j = 1;
	while (j <= y)
	{
		i = 1;
		while (i <= x)
		{
			if (i == 1)
			j++;
		}
		ft_putchar('\n');
		i++;
	}
}
